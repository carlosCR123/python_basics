# Variables
## Best Practices:
### Not use CamelCase use instead snake_case

my_string_variable = "Testing"
print(my_string_variable)

my_int_variable = 90
print(my_int_variable) 

my_bool_variable = False
print(my_bool_variable)

# Dictionary Type - dict
object_info = {
    "name": "Object",
    "type": "Test Type"
}
print(type(object_info))

# Concat in print
print(my_string_variable, my_int_variable)